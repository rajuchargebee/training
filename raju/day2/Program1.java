import java.io.*;
class Program1
{
	public static void main(String args[])throws IOException
	{
		BufferedReader br=new BufferedReader(new InputStreamReader(System.in));
		System.out.println("Enter value for Number of Line=");
		int line=Integer.parseInt(br.readLine());
		int i,j;
		for(i=1;i<=line;i++)
		{
			j=i;
			while(j<line)
			{
				System.out.print("  ");
				j++;
			}
			j=1;
			while(j<=i)
			{
				System.out.print(j+" ");
				j++;
			}
			j--;
			j--;
			while(j>=1)
			{
				System.out.print(j+" ");
				j--;
			}
			System.out.println();
		}
	}
}
